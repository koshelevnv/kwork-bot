def update_categories(new_cats: dict[str, list[tuple[str, str]]]) -> None:
    """Обновляет KWORK_CATEGORIES и CATEGORY_NAME_BY_ID на месте (in-place)."""
    KWORK_CATEGORIES.clear()
    KWORK_CATEGORIES.update(new_cats)
    CATEGORY_NAME_BY_ID.clear()
    CATEGORY_NAME_BY_ID.update({
        cat_id: name
        for cats in KWORK_CATEGORIES.values()
        for cat_id, name in cats
    })

